function GetInventorySlotItem(itemID)
		assert(type(itemID) == "number", "GetInventorySlotItem: wrong argument types (<number> expected)")
		for _, j in pairs({ ITEM_1, ITEM_2, ITEM_3, ITEM_4, ITEM_5, ITEM_6}) do
			if myHero:GetItemData(j).itemID == itemID and myHero:GetSpellData(j).currentCd == 0 then return j end
		end
		return nil
	    end
	
	function UseHydra()
		local HTarget = CurrentTarget(125)
		if HTarget then 
			local hydraitem = GetInventorySlotItem(3748) or GetInventorySlotItem(3077)
			if hydraitem and myHero.attackData.state == STATE_WINDDOWN then
				Control.CastSpell(keybindings[hydraitem],HTarget.pos)
                Control.Attack(HTarget)
			end
		end
	end
    function UseHydraminion()
        for i = 1, Game.MinionCount() do
	    local minion = Game.Minion(i)
        if minion and minion.team == 300 or minion.team ~= myHero.team then 
			local hydraitem = GetInventorySlotItem(3748) or GetInventorySlotItem(3077)
			if hydraitem and myHero.attackData.state == STATE_WINDDOWN then
				Control.CastSpell(keybindings[hydraitem])
                Control.Attack(minion)
			end
		end
        end
	end

    function UseBotrk()
		local BTarget = CurrentTarget(400)
		if BTarget then 
			local botrkitem = GetInventorySlotItem(3153) or GetInventorySlotItem(3144)
			if botrkitem then
				Control.CastSpell(keybindings[botrkitem],BTarget.pos)
			end
		end
	end


function IsRecalling()
	for K, Buff in pairs(GetBuffs(myHero)) do
		if Buff.name == "recall" and Buff.duration > 0 then
			return true
		end
	end
	return false
end

function ValidTarget(target, range)
	range = range and range or math.huge
	return target ~= nil and target.valid and target.visible and not target.dead and target.distance <= range
end

function Nocturne:Tick()
    if myHero.dead or Game.IsChatOpen() == true or IsRecalling() == true then return end
	if self.Menu.HarassMode.harassActive:Value() and self:EnemyInRange(1300) then
		self:Harass()
	end
	if self.Menu.ComboMode.comboActive:Value() and self:EnemyInRange(1300) then
		self:Combo()
	end
	if self.Menu.ClearMode.clearActive:Value() then
		self:Jungle()
	end
end

function Nocturne:HasBuff(unit, buffname)
	for i = 0, unit.buffCount do
		local buff = unit:GetBuff(i)
		if buff.name == buffname and buff.count > 0 then 
			return true
		end
	end
	return false
end

function Nocturne:IsReady(spell)
	return Game.CanUseSpell(spell) == 0
end

function Nocturne:CheckMana(spellSlot)
	return myHero:GetSpellData(spellSlot).mana < myHero.mana
end

function Nocturne:CanCast(spellSlot)
	return self:IsReady(spellSlot)
end

function EnableMovement()
	SetMovement(true)
end

function ReturnCursor(pos)
	Control.SetCursorPos(pos)
	DelayAction(EnableMovement,0.1)
end

function LeftClick(pos)
	Control.mouse_event(MOUSEEVENTF_LEFTDOWN)
	Control.mouse_event(MOUSEEVENTF_LEFTUP)
	DelayAction(ReturnCursor,0.05,{pos})
end
function Nocturne:Draw()
    local textPos = myHero.pos:To2D()
    if self:CanCast(_Q) then Draw.Circle(myHero.pos, 650, 3,  Draw.Color(255, 000, 222, 255)) end
end

function Nocturne:CastSpell(spell,pos)
	local customcast = self.Menu.CustomSpellCast:Value()
	if not customcast then
		Control.CastSpell(spell, pos)
		return
	else
		local delay = self.Menu.delay:Value()
		local ticker = GetTickCount()
		if castSpell.state == 0 and ticker > castSpell.casting then
			castSpell.state = 1
			castSpell.mouse = mousePos
			castSpell.tick = ticker
			if ticker - castSpell.tick < Game.Latency() then
				SetMovement(false)
				Control.SetCursorPos(pos)
				Control.KeyDown(spell)
				Control.KeyUp(spell)
				DelayAction(LeftClick,delay/1000,{castSpell.mouse})
				castSpell.casting = ticker + 500
			end
		end
	end
end

function Nocturne:Combo()
	if (not _G.SDK and not _G.GOS) then return end
	local target = target or (_G.SDK and _G.SDK.TargetSelector:GetTarget(1300, _G.SDK.DAMAGE_TYPE_PHYSICAL)) or (_G.GOS and _G.GOS:GetTarget(E.Range,"AD"))
	if target and target.type == "AIHeroClient" then
    if self.Menu.ComboMode.UseHYDRA:Value() and target.pos:DistanceTo(myHero.pos) < 150 then
        if myHero.attackData.state == STATE_WINDDOWN and not self:CanCast(_Q) then
            UseHydra()
        end
    end

    if self:CanCast(_E) and target.pos:DistanceTo(myHero.pos) < E.Range and self.Menu.ComboMode.UseE:Value() then 
			self:CastSpell(HK_E,target)
	end

	if self:CanCast(_Q) and target.pos:DistanceTo(myHero.pos) < Q.Range and self.Menu.ComboMode.UseQ:Value() then           
        local castpos,HitChance, pos = TPred:GetBestCastPosition(target, Q.Delay , Q.Width, Q.Range,Q.Speed, myHero.pos, Q.ignorecol, Q.Type )
		if (HitChance > 0 ) then
			self:CastSpell(HK_Q,castpos)
		end
	end

    if self:EnemyInRange(400) and not self:CanCast(_Q) then 
        local BTarget = CurrentTarget(400)
        if BTarget then
            if myHero.pos:DistanceTo(BTarget.pos) < 400 then
			    UseBotrk()
            end
        end
    end
	end
end

function Nocturne:GetEnemyHeroes()
	self.EnemyHeroes = {}
	for i = 1, Game.HeroCount() do
		local Hero = Game.Hero(i)
		if Hero.isEnemy then
			table.insert(self.EnemyHeroes, Hero)
		end
	end
	return self.EnemyHeroes
end

function Nocturne:EnemyInRange(range)
	local count = 0
	for i, target in ipairs(self:GetEnemyHeroes()) do
		if target.pos:DistanceTo(myHero.pos) < range then 
			count = count + 1
		end
	end
	return count
end

